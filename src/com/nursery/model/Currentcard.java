package com.nursery.model;

public class Currentcard {
	private String stockNumber;
	private String numbersId;
	private String voucherNum;
	private String stocRateId;
	private String branchId;
	private String manageType;
	private String userId;
	private String operateDate;
	private String password;
	private String flag;
	//---------------------------get and set ------------------------------------------------------------
	public String getStockNumber() {
		return stockNumber;
	}
	public void setStockNumber(String stockNumber) {
		this.stockNumber = stockNumber;
	}
	public String getNumbersId() {
		return numbersId;
	}
	public void setNumbersId(String numbersId) {
		this.numbersId = numbersId;
	}
	public String getVoucherNum() {
		return voucherNum;
	}
	public void setVoucherNum(String voucherNum) {
		this.voucherNum = voucherNum;
	}
	public String getStocRateId() {
		return stocRateId;
	}
	public void setStocRateId(String stocRateId) {
		this.stocRateId = stocRateId;
	}
	public String getBranchId() {
		return branchId;
	}
	public void setBranchId(String branchId) {
		this.branchId = branchId;
	}
	public String getManageType() {
		return manageType;
	}
	public void setManageType(String manageType) {
		this.manageType = manageType;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getOperateDate() {
		return operateDate;
	}
	public void setOperateDate(String operateDate) {
		this.operateDate = operateDate;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getFlag() {
		return flag;
	}
	public void setFlag(String flag) {
		this.flag = flag;
	}
	
}
